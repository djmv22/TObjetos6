    private void cmdCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearActionPerformed
        int capacidadM, cantidadA;
        if(txtCapacidadMaxima.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Digite la capacidad maxima", "Error", JOptionPane.ERROR_MESSAGE);
            txtCapacidadMaxima.requestFocusInWindow();
        }
        else if(Integer.parseInt(txtCapacidadMaxima.getText().trim())==0){
            JOptionPane.showMessageDialog(this, "La capacidad maxima no puede ser cero", "Error", JOptionPane.ERROR_MESSAGE);
            txtCapacidadMaxima.requestFocusInWindow();
            txtCapacidadMaxima.selectAll();
        }
        else if(txtCantidadActual.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Digite la cantidad actual", "Error", JOptionPane.ERROR_MESSAGE);
            txtCantidadActual.requestFocusInWindow();
        }
        else{
            try{
                    capacidadM = Integer.parseInt(txtCapacidadMaxima.getText());
                    cantidadA = Integer.parseInt(txtCantidadActual.getText());
                    c = new Cafetera(capacidadM,cantidadA);
                    JOptionPane.showMessageDialog(this, "Cafetera creada exitosamente");
                    
                    txtCantidadActual.setEditable(false);
                    txtCapacidadMaxima.setEditable(false);
                    txtModificacionCafetera.setEditable(true);
                    cmdCrear.setEnabled(false);
                    cmdCrearAutomatico.setEnabled(false);
                    cmdAgregar.setEnabled(true);
                    cmdServir.setEnabled(true);
                    cmdLlenarCafetera.setEnabled(true);
                    cmdVaciarCafetera.setEnabled(true);
                    cmdMostrar.setEnabled(true);
                    cmdLimpiar.setEnabled(true);
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Valor no valido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cmdCrearActionPerformed

